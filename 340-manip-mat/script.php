<?php

echo ceil(7.35); // arredonda para cima => 8

echo '<br>';

echo floor(7.35); // arredonda para baixo => 7

echo '<br>';

echo round(7.35); // arredonda com base na fração => 7

echo '<br>';

echo rand(0, 300); // gera um valor aleatório entre 0 e 300

echo '<br>';

echo getrandmax(); // retorna o valor máximo aleatório possível na máquina

echo '<br>';

echo sqrt(15); // retorna a raiz quadrada do número passado integer ou float

